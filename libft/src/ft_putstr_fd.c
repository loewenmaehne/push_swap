#include "../include/libft.h"
#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int	strlen;

	strlen = ft_strlen(s);
	if (fd < 0 || !strlen)
		return ;
	write(fd, s, strlen);
}
