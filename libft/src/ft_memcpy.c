#include "../include/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*destp;
	const unsigned char	*srcp;

	if (!dest && !src)
		return (NULL);
	destp = dest;
	srcp = src;
	while (n--)
		*destp++ = *srcp++;
	return (dest);
}
