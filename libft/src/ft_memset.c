#include "../include/libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*p;
	char	uc;

	p = s;
	uc = (char) c;
	while (n-- != 0)
	{
		*p = uc;
		p++;
	}
	return (s);
}
